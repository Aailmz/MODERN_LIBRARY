function returnedBook(loanId, condition, note) {
        Swal.fire({
            title: "Book is Returned?",
            showDenyButton: true,
            showCancelButton: true,
            confirmButtonText: "Returned",
            denyButtonText: `Not yet`
        }).then((result) => {
            if (result.isConfirmed) {
                $.ajax({
                    type: 'POST',
                    url: '{{ route("returned-book") }}', // Laravel helper to generate route URL
                    data: {
                        id: loanId,
                        condition: condition,
                        note: note
                    },
                    headers: {
                        'X-CSRF-TOKEN': $('meta[name="csrf-token"]').attr('content')
                    },
                    success: function(response) {
                        // Handle success response
                        console.log(response);
                        Swal.fire({
                            title: "Success",
                            text: "Book is returned and notes are sended!",
                            icon: "success"
                        }).then((result) => {
                            if (result.isConfirmed) {
                                location.reload();
                            }
                        });
                    },
                    error: function(xhr, status, error) {
                        // Handle error response
                        console.error(xhr.responseText);
                        Swal.fire({
                            title: "Error",
                            text: "An error occurred while rejecting the notification.",
                            icon: "error"
                        });
                    }
                });
            } else if (result.isDenied) {
                // Handle the case where the user denied the action
            }
        });
    }

